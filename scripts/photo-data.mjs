export function parseTitle(stem) {
  const match = stem.match(/^(.+?)\s+\d+°/);
  return match ? match[1].replace(/\s+/g, " ").trim() : stem.trim();
}

export function parseCoords(stem) {
  const match = stem.match(
    /(\d+°\s*\d+'\s*[\d.]+")\s*N\s+(\d+°\s*\d+'\s*[\d.]+\")\s*E/
  );
  if (!match) return null;
  return { lat: match[1].trim(), lng: match[2].trim() };
}

export function dmsToDecimal(dms) {
  const match = dms.match(/(\d+)°\s*(\d+)'\s*([\d.]+)"/);
  if (!match) return null;
  return Number(match[1]) + Number(match[2]) / 60 + Number(match[3]) / 3600;
}

export function toSlug(text) {
  return text
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "")
    .slice(0, 80);
}

export function lookupTitle(cnTitle) {
  const normalized = cnTitle.replace(/\s+/g, " ").trim();
  if (TRANSLATIONS[normalized]) return TRANSLATIONS[normalized];

  for (const [key, value] of Object.entries(TRANSLATIONS)) {
    if (key.replace(/\s+/g, " ").trim() === normalized) return value;
  }

  return { en: normalized, zh: normalized };
}

export function fixKnownBadCoords(coords, titleEn) {
  if (!coords) return coords;
  if (titleEn.includes("Red Dates") && coords.lat.startsWith("7°")) {
    return { ...coords, lat: coords.lat.replace(/^7°/, "37°") };
  }
  return coords;
}
